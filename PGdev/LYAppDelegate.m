#pragma mark - RemoteNotifications
- (void)application:(UIApplication *)application didRegisterForRemoteNotificationsWithDeviceToken:(NSData *)deviceToken {
    // 推送生成token
    const unsigned *tokenBytes = [deviceToken bytes];
    NSString *deviceTokenString = [NSString stringWithFormat:@"%08x%08x%08x%08x%08x%08x%08x%08x",
                                   ntohl(tokenBytes[0]), ntohl(tokenBytes[1]), ntohl(tokenBytes[2]),
                                   ntohl(tokenBytes[3]), ntohl(tokenBytes[4]), ntohl(tokenBytes[5]),
                                   ntohl(tokenBytes[6]), ntohl(tokenBytes[7])];
    
    // 发送给自家的后端 记录下token uuid macAddress
    NSString *macAddress = [[NSUserDefaults standardUserDefaults] objectForKey:@"macAddress"];
    DLog(@"deviceTokenString:%@, macAddress:%@", deviceTokenString, macAddress);
}

